#Модуль math в Python предоставляет доступ к множеству математических функций и констант, 
# которые позволяют выполнять различные математические операции. 
# Этот модуль является частью стандартной библиотеки Python, 
# что означает, что он доступен по умолчанию и не требует дополнительной установки.

x = min(5, 10, 25)
y = max(5, 10, 25)

print(x)
print(y)


x = abs(-7.25)

print(x)


x = pow(4, 3)

print(x)


import math

x = math.sqrt(64)

print(x)



